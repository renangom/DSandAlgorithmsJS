let result;

result = '3' + 2;
console.log(result);

result = '3' + true; 
console.log(result); // "3true"

result = '3' + undefined; 
console.log(result); // "3undefined"

result = '3' + null; 
console.log(result); // "3null"


//implicit conversion
result = '4' - '2'; 
console.log(result); // 2

result = '4' - 2;
console.log(result); // 2

result = '4' * 2;
console.log(result); // 8

result = '4' / 2;
console.log(result); // 2

// if boolean is used, true is 1, false is 0

result = '4' - true;
console.log(result); // 3

result = 4 + true;
console.log(result); // 5

result = 4 + false;
console.log(result); // 4

//empty strings and null returns 0
result = Number(null);
console.log(result);  // 0

result = Number(' ')
console.log(result);  // 0

